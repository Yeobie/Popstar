let namcounter = 1;

let nambutton = document.querySelector("#nambutton");
nambutton.addEventListener("click", function () {
  let nam = document.querySelector("#nam");
  nam.setAttribute(
    "src",
    "https://s3.amazonaws.com/shecodesio-production/uploads/files/000/065/695/original/2nam.png?1675455014"
  );
  namcounter++;
  if (namcounter > 2) {
    namcounter = 1;
    nam.setAttribute(
      "src",
      "https://s3.amazonaws.com/shecodesio-production/uploads/files/000/065/669/original/1Nam.png?1675445283"
    );
  }
});

